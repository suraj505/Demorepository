package com.csi.CoreJunitApplication;

public class ArithmaticApplication {
	public static void main(String[] args) {
      
		System.out.println(sum(4,5));
		System.out.println(multiplication(20,30));
	}
	static int sum(int n1,int n2){
		
		return n1+n2;
	}
	
	static int multiplication(int n3,int n5){
		return n3*n5;
		
	}
}
